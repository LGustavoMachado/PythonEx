nome = (input("Digite seu nome: "))  #Entrada digitar o nome
nome = nome.upper()         #Transforma as letras do nome em mai√∫sculas
nome = nome.replace("A", "@") #Troca o A pelo @
nome = nome.replace("E", "&") #Troca o E pelo &
nome = nome.replace("I", "!") #Troca o I por !
nome = nome.replace("O", "#") #Troca o O por #
nome = nome.replace("U", "*") #Troca o U por *
print("Nome convertido: ",nome) #imprime  o nome convertido
    
    

    
        



